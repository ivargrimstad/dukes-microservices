package eu.agilejava.dukes.department;

import java.io.Serializable;
import javax.persistence.Cacheable;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import static javax.persistence.GenerationType.AUTO;
import static javax.persistence.GenerationType.SEQUENCE;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;
import javax.persistence.UniqueConstraint;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;

/**
 *
 * @author Ivar Grimstad (ivar.grimstad@gmail.com)
 */
@Entity
@Table(name = "department", uniqueConstraints
        = @UniqueConstraint(columnNames = "uuid"))
public class Department implements Serializable {

    private static final long serialVersionUID = 675831561365333123L;

    @Id
   @GeneratedValue(strategy = GenerationType.SEQUENCE, generator = "department_seq")
   @SequenceGenerator(name = "department_seq", sequenceName = "department_seq", allocationSize = 1, initialValue = 10)
    @Column(name = "id")
    private Long id;

    @Column(name = "uuid")
    private String uuid;

    @Column(name = "name")
    @NotNull
    @Size(min = 2, max = 30, message = "ett riktigt namn tack!!")
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
